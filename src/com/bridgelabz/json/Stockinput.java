package com.bridgelabz.json;

public class Stockinput {
private String stock_name;

private int no_of_stocks;
private int share_price;
private int total_price;
public String getStock_name() {
	return stock_name;
}
public void setStock_name(String stock_name) {
	this.stock_name = stock_name;
}
public int getNo_of_stocks() {
	return no_of_stocks;
}
public void setNo_of_stocks(int no_of_stocks) {
	this.no_of_stocks = no_of_stocks;
}
public int getShare_price() {
	return share_price;
}
public void setShare_price(int share_price) {
	this.share_price = share_price;
}
public int getTotal_price() {
	return total_price;
}
public void setTotal_price(int total_price) {
	this.total_price = total_price;
}

}
