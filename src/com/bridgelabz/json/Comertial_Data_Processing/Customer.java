package com.bridgelabz.json.Comertial_Data_Processing;

public class Customer {
private String name;
private int yesbank;
private int idfc;
private int eichermotor;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getYesbank() {
	return yesbank;
}
public void setYesbank(int yesbank) {
	this.yesbank = yesbank;
}
public int getIdfc() {
	return idfc;
}
public void setIdfc(int idfc) {
	this.idfc = idfc;
}
public int getEichermotor() {
	return eichermotor;
}
public void setEichermotor(int eichermotor) {
	this.eichermotor = eichermotor;
}
}
